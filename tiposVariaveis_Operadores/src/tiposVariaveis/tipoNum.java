package tiposVariaveis;


public class tipoNum {

	public static void main(String[] args) {

		Byte numeroByte = 1; // -128 até 127		
		Short numeroShort = 1; // -32768 até 32767		
		Integer numeroInt = 1; // -2147483648 até 2147483647 (2 elevado a 31 potência)		
		Long numeroLog = 1l; // -9223372036854775808 até 9223372036854775807 (2 elevado a 63 potência)
		
		Float numeroFloat = 1.0f; //numeros reais
		Double numeroDouble = 1.0; //numeros reais

	}

}
